MAX_STAGE = len(HANGMAN_FRAMES) - 1


def get_frame(stage: int) -> str:
    """
    Returns the requested frame.

    Any stage outside the valid range is clamped.
    """

    if stage < 0:
        stage = 0

    if stage > MAX_STAGE:
        stage = MAX_STAGE

    return HANGMAN_FRAMES[stage]